# ----------------------------------------------------------------------------------------------------------------------
from sdoc.sdoc2.node.Node import Node


class LinesNode(Node):
    """
    SDoc2 node for lines.
    """
    # ------------------------------------------------------------------------------------------------------------------
    def __init__(self):
        super().__init__('line')

    # ------------------------------------------------------------------------------------------------------------------
    def is_block_command(self):
        """
        Returns False.

        :rtype: bool
        """
        return False

    # ------------------------------------------------------------------------------------------------------------------
    def is_inline_command(self):
        """
        Returns False.

        :rtype: bool
        """
        return False

    # ------------------------------------------------------------------------------------------------------------------
    def is_phrasing(self):
        """
        Returns True.

        :rtype: bool
        """
        return True


